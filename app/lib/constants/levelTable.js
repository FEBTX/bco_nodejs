LevelTable.findLevel = function(key) {
	var lo = 0;
	var hi = LevelTable.level.length - 1;
	while (lo <= hi) {
		// Key is in a[lo..hi] or not present.
		var mid = lo + Math.floor((hi - lo) / 2);
		if (key < LevelTable.level[mid])
			hi = mid - 1;
		else if (key > LevelTable.level[mid])
			lo = mid + 1;
		else
			return mid;
	}
	return -1;
};